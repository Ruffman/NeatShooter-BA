#include "DestructibleMovementComponent.h"



void UDestructibleMovementComponent::TickComponent(float DeltaTime, enum ELevelTick TickType, FActorComponentTickFunction *ThisTickFunction)
{
	Super::TickComponent(DeltaTime, TickType, ThisTickFunction);

	// Make sure that everything is still valid, and that we are allowed to move.
	if (!UpdatedComponent || ShouldSkipUpdate(DeltaTime))
	{
		return;
	}

	FVector CurrentLocation = this->GetActorLocation();

	FVector DesiredMovementThisFrame = FVector(-m_VertSpeed, 0.f, 0.f) * DeltaTime;
	if (!DesiredMovementThisFrame.IsNearlyZero())
	{
		FHitResult Hit;
		SafeMoveUpdatedComponent(DesiredMovementThisFrame, UpdatedComponent->GetComponentRotation(), true, Hit);
	}
}

void UDestructibleMovementComponent::BeginPlay()
{
	Super::BeginPlay();

}

void UDestructibleMovementComponent::SetVertSpeed(float VertSpeed)
{
	m_VertSpeed = VertSpeed;
}
